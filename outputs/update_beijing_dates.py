def update():
    conn = sqlite3.connect(DB)
    cur = conn.cursor()
    # 仅处理北京市、当前已存在的记录
    cur.execute("SELECT id, program, round_no, sample_date, due_date FROM eqa_plans WHERE org='北京市'")
    rows = cur.fetchall()
    changed = 0
    skipped = []
    unchanged = []
    for id_, prog, rnd, old_sd, old_dd in rows:
        key = (prog, rnd)
        if key in BEIJING_DATES:
            new_sd, new_dd = BEIJING_DATES[key]
            if (old_sd or '') != new_sd or (old_dd or '') != new_dd:
                cur.execute(
                    'UPDATE eqa_plans SET sample_date=?, due_date=?, updated_at=? WHERE id=?',
                    (new_sd, new_dd, date.today().isoformat(), id_)
                )
                changed += 1
                print(f'UPDATED id={id_}: {prog} {rnd}  {old_sd or "NULL"} / {old_dd or "NULL"} -> {new_sd or "NULL"} / {new_dd or "NULL"}')
            else:
                unchanged.append((id_, prog, rnd))
        else:
            skipped.append((id_, prog, rnd, old_sd, old_dd))
    conn.commit()
    print(f'\n总计：已更新 {changed} 行，未变动 {len(unchanged)} 行，图片未覆盖保留原样 {len(skipped)} 行')
    if skipped:
        print('\n以下记录因图片未拍到对应项（第3/4页缺失）保留原样：')
        for id_, prog, rnd, old_sd, old_dd in skipped:
            print(f'  id={id_}: {prog} {rnd}  {old_sd or "NULL"} / {old_dd or "NULL"}')
    conn.close()


if __name__ == '__main__':
    update()
